async function readJsonOrDefault<T>(relativePath: string, fallback: T): Promise<T> {
  try {
    const fullPath = await PathGuard.validatePath(relativePath, "read");
    return JSON.parse(await fs.readFile(fullPath, "utf-8")) as T;
  } catch {
    return fallback;
  }
}

async function renderCreatureSvgForDashboard(rootDir: string): Promise<string> {
  const config = await readJsonOrDefault("brain/config.json", {
    name: "hatchling",
    createdAt: rootDir,
  });
  const mutationState = await readJsonOrDefault("brain/mutation_state.json", {
    totalMutations: 0,
    successfulMutations: 0,
    sleepCycles: 0,
  });
  const curiosity = await readJsonOrDefault("brain/curiosity_state.json", { adjustedCuriosity: 5 });
  const quotas = await readJsonOrDefault("brain/quotas.json", {
    tokens: { today: 0, maxPerDay: 100000 },
  });
  const heartbeat = await readJsonOrDefault("brain/heartbeat.json", { lowEnergy: false });
  const health = await checkHealth();
  const commitCount = parseInt(execSync("git rev-list --count HEAD", { cwd: rootDir, encoding: "utf-8" }).trim(), 10);
  const tokenUsagePercent = (quotas.tokens.today / quotas.tokens.maxPerDay) * 100;
  const energyLevel = tokenUsagePercent > 90 ? "Critical" : tokenUsagePercent > 70 ? "Low" : "High";
  const seed = `${config.name || "hatchling"}:${config.createdAt || rootDir}`;
  const genome = await loadGenome(rootDir, seed);
  const creature = renderCreature({
    seed,
    commitCount,
    sleepCycles: Number(mutationState.sleepCycles || 0),
    successfulMutations: Number(mutationState.successfulMutations || 0),
    totalMutations: Number(mutationState.totalMutations || 0),
    curiosity: Number(curiosity.adjustedCuriosity || 5),
    energyLevel,
    safeMode: Boolean(health.safeMode),
    lowEnergy: Boolean(heartbeat.lowEnergy),
    palette: genome.palette,
    body: genome.body,
    eyes: genome.eyes,
    accent: genome.accent,
  });
  return renderCreatureSvg(creature, {
    seed,
    commitCount,
    sleepCycles: Number(mutationState.sleepCycles || 0),
    successfulMutations: Number(mutationState.successfulMutations || 0),
    totalMutations: Number(mutationState.totalMutations || 0),
    curiosity: Number(curiosity.adjustedCuriosity || 5),
    energyLevel,
    safeMode: Boolean(health.safeMode),
    lowEnergy: Boolean(heartbeat.lowEnergy),
    palette: genome.palette,
    body: genome.body,
    eyes: genome.eyes,
    accent: genome.accent,
  });
}

async function renderCreatureStatusChips(rootDir: string): Promise<string> {
  const social = await readJsonOrDefault("brain/social_memory.json", {
    users: {},
  } as { users: Record<string, { relationshipStage?: string }> });
  const strategy = await readJsonOrDefault("brain/autonomy_strategy.json", {
    goals: [],
  } as { goals: Array<{ status?: string }> });
  const events = await summarizeCreatureEvents(rootDir);
  const trusted = Object.values(social.users || {}).filter((u) => String(u.relationshipStage || "") === "trusted").length;
  const familiar = Object.values(social.users || {}).filter((u) => String(u.relationshipStage || "") === "familiar").length;
  const pendingGoals = (strategy.goals || []).filter((goal) => String(goal.status || "") === "pending").length;
  const chips = [
    { text: `trusted bonds: ${trusted}`, activity: false },
    { text: `familiar bonds: ${familiar}`, activity: false },
    { text: `pending goals: ${pendingGoals}`, activity: false },
    { text: `creature events: ${events.total}`, activity: true },
    { text: `recent event: ${events.recentTypes.slice(-1)[0] || "none"}`, activity: true },
  ];
  return chips
    .map((value) => `<span class="chip${value.activity ? " activity" : ""}">${escapeHtml(value.text)}</span>`)
    .join("");
}

export async function startWebDashboard(rootDir: string, port: number): Promise<http.Server> {
  const server = http.createServer(async (_req, res) => {
    try {
      const html = await renderWebDashboard(rootDir);
      res.writeHead(200, { "content-type": "text/html; charset=utf-8" });
      res.end(html);
    } catch (error: any) {
      res.writeHead(500, { "content-type": "text/plain; charset=utf-8" });
      res.end(`Web dashboard error: ${error?.message || String(error)}`);
    }
  });

  await new Promise<void>((resolve, reject) => {
    server.once("error", reject);
    server.listen(port, "127.0.0.1", () => resolve());
  });
  return server;
}
